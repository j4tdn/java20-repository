package operator;

public class Ex01BasicOperatorDemo {
	public static void main(String[] args) {
		int a = 5;
		
		a = a + 2; // 7
		
		a -= 3; // 4
		
		System.out.println("a --> " + a);
		
		// operator: ++ -- --> prefix, suffix
		
		
		
		
		int b = ++a;
		System.out.println("b --> " + b);
	}

}
