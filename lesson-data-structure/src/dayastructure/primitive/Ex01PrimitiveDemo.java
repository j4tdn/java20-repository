package dayastructure.primitive;

public class Ex01PrimitiveDemo {
      public static void main(String[] args) {
		// declaration ---> khai báo
    	  int digit = 23;
    	  float point = 8.6f; // thêm 'f' vào khi giá trị là số thực
    	  char letter = 'A';
    	  long number =  2111111111l; // thêm 'l' vaò khi giá trị vượt quá
    	  
    	// invoke --> gọi sử dụng
    	  System.out.println("digit " + digit);
    	  System.out.println("point " + point);
    	  System.out.println("letter " + letter);
    	  System.out.println("numbber " + number);
	}
}
